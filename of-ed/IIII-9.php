            <p class="content-text">
                <br>   
                a.	Trayectos formativos
               <br>
                -      El trayecto Bases teórico – metodológicas para la enseñanza está conformado por 10 cursos que contienen actividades de docencia de tipo teórico-práctico, con una carga académica semanal de 4/4.5 y 6/6.75 horas/créditos de trabajo presencial.
               <br>
                -      El trayecto Formación para la enseñanza y el aprendizaje está integrado por 24 cursos que articulan actividades de carácter teórico y práctico, centradas en el aprendizaje de los conocimientos disciplinarios y su enseñanza, con una carga académica semanal de 4/4.5 y 6/6.75 horas/créditos de trabajo presencial.
               <br>
                -      El trayecto Práctica profesional está integrado por ocho cursos. Del primero al séptimo semestre, los cursos articulan actividades de tipo teórico-práctico, con énfasis en el acercamiento paulatino a la actividad profesional en contextos específicos y su análisis. Los cursos de primer y segundo semestre tienen una carga académica de 4 horas semanales y 4.5 créditos. Los cursos de tercer a séptimo semestre tienen una carga académica de 6 horas semanales y un valor de 6.75 créditos. El último curso de este trayecto, ubicado en el octavo semestre, es un espacio curricular de práctica profesional en la escuela primaria indígena, con una duración de 20 horas a desarrollarse durante 16 semanas, con un valor de 6.4 créditos.
               <br>
                -      El trayecto de cursos Optativos se compone de cuatro espacios curriculares para una formación complementaria e integral del estudiante con 4 horas semanales de carga académica y un valor 4.5 créditos. Contiene actividades de docencia de tipo teórico, práctico, a distancia o mixto.
               <br><br>
                a.	Lengua adicional
               <br>
                -      Tres espacios curriculares para el estudio del Inglés como lengua adicional con 6 horas semanales de carga académica y un valor de 6.75 créditos de trabajo presencial.
               <br><br>
                a.	Trabajo de titulación
               <br>
                -      El trabajo de titulación, corresponde al tiempo de dedicación que el estudiante normalista destinará para la elaboración de: portafolio de evidencias, informe de prácticas profesionales o tesis de investigación. Tiene una carga horaria de 4 horas semanales, a cubrir durante 54 semanas y tiene un valor de 10.8 créditos académicos que corresponden a actividades de aprendizaje individual o independiente a través de asesoría por parte de un docente.

