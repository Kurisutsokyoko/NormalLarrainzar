            <h2 class="sub-tilte-content">Art. 32.- El personal docente de las normales tendrá las funciones y obligaciones siguientes:</h2>
            <p class="text-content">
                    I. Impartir educación para formar docentes para el tipo de educación básica, con intensidad y calidad que esta requiere, de acuerdo a los programas educativos vigentes, 
                    y demás actividades que le sean asignados por la Subdirección Académica.
                <br>
                <br>
                    II. Cumplir con el horario de trabajo que, conforme a la carga horaria contratada, de acuerdo a su nombramiento, le establezca el Subdirector Académico, debiendo 
                    registrar su asistencia conforme a las normas de control vigentes.
                <br>
                <br>
                    III. Conocer y cumplir con el plan y programas de estudio vigente, así como emplear adecuadamente el material didáctico disponible.
                <br>
                <br>
                    IV. Cumplir con las funciones que tiene encomendadas, inherentes a su responsabilidad laboral e informar oportunamente a la subdirección académica las incidencias 
                    especificas que impidan su cumplimiento u omisión de actuación incidirá en la aplicación de la normatividad vigente.
                <br>
                <br>
                    V. Organizar y realizar investigaciones sobre problemas socio-pedagógicos de interés regional, nacional e internacional, de acuerdo al programa anual de trabajo de 
                    la institución.
                <br>
                <br>
                    VI. Desarrollar actividades orientadas a extender los beneficios de la ciencia, la técnica y la cultura.
                <br>
                <br>
                    VII. Coadyuvar en la formación ética y profesional de los estudiantes del plantel.
                <br>
                <br>
                    VIII. Participar en la gestión y ejecución de las actividades que la dirección, subdirecciones o la autoridad educativa respectiva le encomiende.
                <br>
                <br>
                    IX. Actualizar continuamente sus conocimientos, preferentemente en las asignaturas que imparta, de acuerdo con los programas de superación académica profesional, 
                    establecidos por las autoridades.
                <br>
                <br>
                    X. Aplicar exámenes de acuerdo con el calendario y normas oficiales correspondientes y permitir a la subdirección académica la documentación respectiva dentro de los 
                      plazos que le sean fijados.
                <br>
                <br>
                    XI. No suspender labores sin que medie autorización del director del plantel, debiendo justificar sus inasistencias y retardos a través de los mecanismo institucionales 
                       establecidos.
                <br>
                <br>
                    XII. Registrar las asistencias de los alumnos en sus clases para los efectos que procedan normativamente.
                <br>
                <br>
                    XIII. Observar buena conducta dentro y fuera de la institución.
                <br>
                <br>
                    XIV. Responsabilizarse de las buenas condiciones de todos los equipos y materiales que le sean proporcionados para el desempeño de sus labores.
                <br>
                <br>
                    XV. Colaborar en la conservación de los inmuebles, muebles, equipo, material educativo y demás bienes de la escuela.
                <br>
                <br>
                    XVI. Apoyar a la dirección de la escuela en la realización de acciones extraordinarias que le sean solicitadas y que sean necesarias para la buena marcha de la institución.
                <br>
                <br>
                    XVII. No impartir clases particulares remuneradas, ni hacer transacciones mercantiles con los alumnos de la escuela normal donde labora.
                <br>
                <br>
                    XVIII. No modificar los horarios de clases, salvo aprobación de las autoridades del plantel.
                <br>
                <br>
                    XIX. No abandonar el servicio sin causa justificada una vez que ha registrado su asistencia.
                <br>
                <br>
                    XX. No aprovechar el servicio del personal de la institución en asuntos particulares o ajenos a los oficiales a los que están asignados.
                <br>
                <br>
                    XXI. No proporcionar, sin la debida autorización de la dirección de la normal, o de autoridad competente, documentos, datos e informes oficiales del centro educativo.
                <br>
                <br>
                    XXII. Las demás que se establezcan las disposiciones legales y normativas aplicables a su investidura, categoría y funciones.
            </p>
